#include "../includes/libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	dst_len;

	dst_len = ft_strlen(dst);
	if (size < (dst_len + 1))
		return (size + ft_strlen(src));
	ft_strncat(dst, src, (size - dst_len - 1));
	return (dst_len + ft_strlen(src));
}
